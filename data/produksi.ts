export const BULAN_ORDER = ["Juli","Agustus","September","Oktober","November","Desember"]

export function getStatsBulan(bulan: string) {
  const data = bulan === "Semua"
    ? dataProduksi
    : dataProduksi.filter(d => d.month === bulan)
  if (!data.length) return null
  const hdpValues = data.map(d => d.hdp)
  const avgHdp = hdpValues.reduce((a, b) => a + b, 0) / hdpValues.length
  const maxHdp = Math.max(...hdpValues)
  const minHdp = Math.min(...hdpValues)
  const totalVol = data.reduce((a, d) => a + d.vol, 0)
  const totalAct = data.reduce((a, d) => a + d.act, 0)
  const lastData = data[data.length - 1]
  return {
    avgHdp: Math.round(avgHdp * 10) / 10,
    maxHdp: Math.round(maxHdp * 10) / 10,
    minHdp: Math.round(minHdp * 10) / 10,
    totalVol: Math.round(totalVol * 10) / 10,
    totalAct,
    lastHdp: Math.round(lastData.hdp * 10) / 10,
    lastAyam: lastData.ayam,
    hari: data.length,
  }
}

export function getRekapBulanan() {
  return BULAN_ORDER.map(bulan => {
    const data = dataProduksi.filter(d => d.month === bulan)
    const avg = data.reduce((a, d) => a + d.hdp, 0) / data.length
    const totalVol = data.reduce((a, d) => a + d.vol, 0)
    return {
      bulan,
      avgHdp: Math.round(avg * 10) / 10,
      totalVol: Math.round(totalVol * 10) / 10,
      hari: data.length,
    }
  })
}
